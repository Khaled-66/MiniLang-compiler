// TEST 05: If / Else
x = 8;
if (x > 5) {
    print "x is greater than 5";
} else {
    print "x is not greater than 5";
}

// Nested if-else
score = 75;
if (score >= 90) {
    print "A";
} else {
    if (score >= 75) {
        print "B";
    } else {
        print "C";
    }
}


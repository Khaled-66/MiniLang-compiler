// TEST 04: Booleans — literals, logical operators, comparisons
a = true;
b = false;
print a;
print b;

// Logical AND
print true && true;
print true && false;

// Logical OR
print false || true;
print false || false;

// Logical NOT
print !true;
print !false;

// Comparison operators
x = 10;
print x > 5;
print x < 5;
print x >= 10;
print x <= 9;
print x == 10;
print x != 10;
